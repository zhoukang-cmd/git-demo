package com.fourm.entity;
/**
 * 级联菜单  键值对    
 * 工况对比    键值对
 * */
public class KeyAndValue {
	private String keyString;
	private String valueString;
	private String compareValueString;
	public KeyAndValue(){}
	public KeyAndValue(String keyString, String valueString) {
		this.keyString = keyString;
		this.valueString = valueString;
	}

	public String getKeyString() {
		return keyString;
	}

	public void setKeyString(String keyString) {
		this.keyString = keyString;
	}

	public String getValueString() {
		return valueString;
	}

	public void setValueString(String valueString) {
		this.valueString = valueString;
	}
	public String getCompareValueString() {
		return compareValueString;
	}
	public void setCompareValueString(String compareValueString) {
		this.compareValueString = compareValueString;
	}
	
}
