package com.fourm.entity;

/**
 * 历史故障
 * */
public class Fault {

	private int faultId;
	private String faultCreateTime;
	private String faultTime;
	private String faultPosition;
	private String faultField;
	private String faultLevel;
	private String faultTrend;
	
	private int equipId;

	
	public Fault(){}
	
	public int getFaultId() {
		return faultId;
	}
	public void setFaultId(int faultId) {
		this.faultId = faultId;
	}
	public String getFaultCreateTime() {
		return faultCreateTime;
	}
	public void setFaultCreateTime(String faultCreateTime) {
		this.faultCreateTime = faultCreateTime;
	}
	public String getFaultTime() {
		return faultTime;
	}
	public void setFaultTime(String faultTime) {
		this.faultTime = faultTime;
	}
	public String getFaultPosition() {
		return faultPosition;
	}
	public void setFaultPosition(String faultPosition) {
		this.faultPosition = faultPosition;
	}
	public String getFaultLevel() {
		return faultLevel;
	}
	public void setFaultLevel(String faultLevel) {
		this.faultLevel = faultLevel;
	}
	public String getFaultTrend() {
		return faultTrend;
	}
	public void setFaultTrend(String faultTrend) {
		this.faultTrend = faultTrend;
	}
	public int getEquipId() {
		return equipId;
	}
	public void setEquipId(int equipId) {
		this.equipId = equipId;
	}

	public String getFaultField() {
		return faultField;
	}

	public void setFaultField(String faultField) {
		this.faultField = faultField;
	}



	
	
}
